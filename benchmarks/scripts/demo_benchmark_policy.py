from __future__ import annotations

from dataclasses import dataclass


@dataclass(frozen=True)
class BenchmarkFamily:
    family_id: str
    category: str
    title: str
    variant_axes: tuple[str, ...]
    direct_exports: tuple[str, ...]
    represented_exports: tuple[str, ...] = ()
    skipped_exports: tuple[str, ...] = ()


@dataclass(frozen=True)
class ExportCoverage:
    export_id: str
    family_id: str
    category: str
    status: str


def _fns(module: str, *names: str) -> tuple[str, ...]:
    return tuple(f"{module}::{name}" for name in names)


def _methods(module: str, owner: str, *names: str) -> tuple[str, ...]:
    return tuple(f"{module}::{owner}::{name}" for name in names)


