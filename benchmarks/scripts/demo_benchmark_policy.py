def coverage_index() -> dict[str, ExportCoverage]:
    index: dict[str, ExportCoverage] = {}
    for family in BENCHMARK_FAMILIES:
        for status, exports in (
            ("direct", family.direct_exports),
            ("represented_by", family.represented_exports),
            ("skip", family.skipped_exports),
        ):
            for export_id in exports:
                if export_id in index:
                    raise ValueError(f"duplicate policy entry for {export_id}")
                index[export_id] = ExportCoverage(
                    export_id=export_id,
                    family_id=family.family_id,
                    category=family.category,
                    status=status,
                )
    return index


def family_index() -> dict[str, BenchmarkFamily]:
    return {family.family_id: family for family in BENCHMARK_FAMILIES}
